const mongoose = require("mongoose");

const indicatorSchema = new mongoose.Schema({
  id: {
    type: String,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  }
});

indicatorSchema.index({ id: 1 });

module.exports = mongoose.model("Indicator", indicatorSchema);
